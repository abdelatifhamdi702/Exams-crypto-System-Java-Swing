private String[] getCells(String enrg){
String cells[]=new String[18];
int j=0;
for(int k=0;k<18;k++){
cells[k]="";
while(j<enrg.length()){
if (enrg.charAt(j)==';'){
j++; break;
} else{
cells[k]+=enrg.charAt(j); j++;
}}}
return cells;}
