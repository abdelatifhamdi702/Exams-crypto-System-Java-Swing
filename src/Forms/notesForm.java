int decode(String code)
	{String alphabets = "AZERTYUIOPQSDFGHJKLMWXCVBN";
		int n = code.length() - 1;

		int numero = 0;

		for (int i = n; i > 0; i = i-2)
		{
			char alphabet = code.charAt(i-1);
			int index = alphabets.indexOf(alphabet);
			int divider = charToInt(code.charAt(i));

			int portion = divider * 26 + index;

			numero = numero*100 + portion;
		}

		return numero;
	}

	int charToInt(char symbol)
	{
		return symbol % 48;
	}
